void main() {
  // Demonstration of int data type
  int age = 26;

  // Demonstration of double data type
  double weight = 75.5;

  // Demonstration of String data type
  String name = 'John ky';

  // Demonstration of List data type (dynamic)
  List<dynamic> fruits = ['pawpaw', 'peach', 'orange'];

  // Demonstration of Map data type
  Map<String, int> studentScores = {
    'kelly': 40,
    'john': 80,
    'marry': 60,
  };

  // Printing values
  print('Age: $age');
  print('Weight: $weight');
  print('Name: $name');
  print('Fruits: $fruits');
  print('Student Scores: $studentScores');
}